p sprintf("%d", 100)		#=> "100"
p sprintf("%8d", 100)		#=> "     100"
p sprintf("%-8d", 100)		#=> "100     "
p sprintf("%08d", 100)		#=> "00000100"
p sprintf("%08d", -100)		#=> "-0000100"
p sprintf("%.8d", 100)		#=> "00000100"
p sprintf("%10.8d", 100)		#=> "  00000100"
p sprintf("%-10.8d", 100)		#=> "00000100  "
